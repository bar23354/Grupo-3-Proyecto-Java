package elmeromer;

//*Revisar si la palabra es palindrome*/
public class Interfaz {
    public static boolean esPalindrome(String word) {
        int start = 0;
        int end = word.length() - 1;
        while (start < end) {
            if (word.charAt(start) != word.charAt(end)) {
                return false;
            }
            start++;
            end--;
        }
        return true;
    }
}
